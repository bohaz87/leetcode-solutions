type SudoItem = "." | "1" | "2" | "3" | "4" | "5" | "6" | "7" | "8" | "9";

const isValidSudoku = function (board: SudoItem[][]): boolean {
  const isValidBox = function (...box: SudoItem[]): boolean {
    const check = Array(9);
    for (const ch of box) {
      if (ch === ".") continue;

      if (check[ch]) {
        return false;
      } else {
        check[ch] = true;
      }
    }
    return true;
  };

  for (let i = 0; i < 9; i++) {
    if (!isValidBox(...board[i])) {
      return false;
    }

    const column = Array(9);
    for (let j = 0; j < 9; j++) {
      column[j] = board[j][i];
    }

    if (!isValidBox(...column)) {
      return false;
    }
  }

  for (let i = 0; i < 9; i = i + 3) {
    for (let j = 0; j < 9; j = j + 3) {
      if (
        !isValidBox(
          board[i][j],
          board[i][j + 1],
          board[i][j + 2],
          board[i + 1][j],
          board[i + 1][j + 1],
          board[i + 1][j + 2],
          board[i + 2][j],
          board[i + 2][j + 1],
          board[i + 2][j + 2]
        )
      ) {
        return false;
      }
    }
  }

  return true;
};

